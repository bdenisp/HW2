# описание объекта "пациент"

"""
Объект описан следующими переменными:
name - имя
surname - фамилия
age - возраст, лет
growth - рост, см
weight - вес, кг
condition - состояние, True - здоров, False - болен
"""

name = "Oleg"
surname = "Danilov"
age = 30
growth = 179.5
weight = 80.7
condition = True

print ("Переменная name, значение", name, ", тип", type(name))
print ("Переменная surname, значение, ", surname, ", тип", type(surname))
print ("Переменная age, значение", age, ", тип", type(age))
print ("Переменная growth, значение", growth, ", тип", type(growth))
print ("Переменная weight, значение", weight, ", тип", type(weight))
print ("Переменная condition, значение", condition, ", тип", type(condition))
